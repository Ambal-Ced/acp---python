num1 = float(input("Enter First Number: "))
num2 = float(input("Enter Second Number: "))

print("Addition")
sum = num1 + num2
print(num1, "+",num2, "=", sum)

print("Subtraction")
dif= num1 - num2
print(num1, "-",num2, "=", dif)

print("Multiplication")
pro= num1*num2
print(num1, "*",num2, "=", pro)

print("Division")
quo= num1/num2
print(num1, "/",num2, "=", quo)